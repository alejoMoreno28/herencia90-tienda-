function unique(items) {
  return [...new Set(items.filter(Boolean).map((item) => item.toLowerCase()))];
}

function seasonAliases(rawSeason) {
  const numbers = rawSeason.match(/\d{2,4}/g) || [];
  const shortYears = numbers.map((part) => part.slice(-2));
  const compact = rawSeason.replace(/\s+/g, '');
  return unique([
    rawSeason,
    compact,
    compact.replace(/\//g, '-'),
    compact.replace(/-/g, '/'),
    numbers.join('-'),
    numbers.join('/'),
    ...shortYears,
    numbers.length >= 2 ? `${numbers[0]}-${numbers[1].slice(-2)}` : '',
    numbers.length >= 2 ? `${numbers[0]}/${numbers[1].slice(-2)}` : '',
  ]);
}

function createReference({
  slug,
  equipo,
  temporada,
  variante = 'local',
  categoria,
  tipo,
  player,
  aliases = [],
}) {
  const teamAliases = unique([equipo, ...(TEAM_ALIASES[equipo] || [])]);
  const seasonTokens = seasonAliases(temporada);
  const variantAliases = unique(VARIANT_ALIASES[variante] || []);
  const extraAliases = unique([...(player ? [player] : []), ...aliases]);

  return {
    slug,
    equipo,
    temporada,
    variante,
    categoria,
    tipo,
    player: player || '',
    teamAliases,
    seasonTokens,
    variantAliases,
    extraAliases,
    aliases: unique([
      ...teamAliases,
      ...seasonTokens,
      ...variantAliases,
      ...extraAliases,
    ]),
  };
}

